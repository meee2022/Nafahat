/**
 * يُرجع رقم اليوم في السنة (1..366).
 */
function dayOfYear(date: Date = new Date()): number {
  const start = new Date(date.getFullYear(), 0, 0);
  const diff = date.getTime() - start.getTime();
  return Math.floor(diff / 86400000);
}

/**
 * يُرجع آية اليوم - تدور تلقائياً، نفس الآية لكل المستخدمين في نفس اليوم.
 */
export function getAyahOfTheDay(date: Date = new Date()): FeaturedAyah {
  const idx = dayOfYear(date) % FEATURED_AYAHS.length;
  return FEATURED_AYAHS[idx];
}
