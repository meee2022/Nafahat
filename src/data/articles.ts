export function getArticleById(id: string): Article | undefined {
  return ARTICLES.find((a) => a.id === id);
}

/**
 * يجلب مقالات بفئة محدّدة.
 */
export function getArticlesByCategory(cat: ArticleCategory): Article[] {
  return ARTICLES.filter((a) => a.category === cat);
}

/**
 * يجلب المقالات المتميّزة (featured) - للـ home/carousels.
 */
export function getFeaturedArticles(): Article[] {
  return ARTICLES.filter((a) => a.featured);
}

/**
 * يجلب أحدث المقالات (sorted by publishedAt desc).
 */
export function getLatestArticles(limit?: number): Article[] {
  const sorted = [...ARTICLES].sort((a, b) => b.publishedAt - a.publishedAt);
  return limit ? sorted.slice(0, limit) : sorted;
}

/**
 * يبحث في عناوين المقالات وموجزها.
 */
export function searchArticles(query: string): Article[] {
  const q = query.trim();
  if (!q) return [];
  const ql = q.toLowerCase();
  return ARTICLES.filter(
    (a) =>
      a.title.includes(q) ||
      a.excerpt.includes(q) ||
      a.tags.some((t) => t.includes(q)) ||
      a.body.toLowerCase().includes(ql),
  );
}

