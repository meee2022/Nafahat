// ════ Helpers ════

/**
 * يجلب metadata لمجموعة بـ id.
 */
export function getCollectionMeta(id: HadithCollection): HadithCollectionMeta | undefined {
  return HADITH_COLLECTIONS.find((c) => c.id === id);
}

/**
 * يجلب أحاديث مجموعة محدّدة.
 */
export function getHadithsByCollection(id: HadithCollection): Hadith[] {
  return HADITHS.filter((h) => h.collection === id);
}

/**
 * يجلب أحاديث فئة محدّدة (موضوع).
 */
export function getHadithsByCategory(cat: HadithCategory): Hadith[] {
  return HADITHS.filter((h) => h.category === cat);
}

/**
 * بحث في متن الحديث/الراوي/التخريج.
 */
export function searchHadiths(query: string): Hadith[] {
  const q = query.trim();
  if (!q) return [];
  return HADITHS.filter(
    (h) =>
      h.text.includes(q) ||
      h.narrator.includes(q) ||
      (h.reference?.includes(q) ?? false),
  );
}

/**
 * يحسب عدد الأحاديث لكل مجموعة (للعرض في chips).
 */
export function getCollectionCounts(): Record<HadithCollection, number> {
  const counts: Record<string, number> = {};
  for (const c of HADITH_COLLECTIONS) counts[c.id] = 0;
  for (const h of HADITHS) counts[h.collection] = (counts[h.collection] ?? 0) + 1;
  return counts as Record<HadithCollection, number>;
}
