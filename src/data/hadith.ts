/**
 * 📜 مجموعة الأحاديث النبوية في نَفَحات.
 *
 * تصنيف مزدوج:
 *  1. collection: الكتاب/المجموعة (الأربعون النووية، صحيح البخاري، إلخ)
 *  2. category: الموضوع (الإيمان، الأخلاق، إلخ)
 *
 * المرجع: نصوص الأحاديث من الكتب التسعة المعتمدة.
 *  - الأربعون النووية: مكتملة (42 حديث - النووي اختار 42 لا 40)
 *  - صحيح البخاري: مختارات من أصحّ الأحاديث الجوامع
 *  - صحيح مسلم: مختارات بنفس المعيار
 *  - السنن (الترمذي، أبو داود، النسائي، ابن ماجه): مختارات
 *  - المسانيد + المختارات: أحاديث مشهورة
 */

export interface Hadith {
  id: string;
  /** رقم الحديث في كتابه الأصلي (مثلاً: الأربعون النووية رقم 1) */
  number?: number;
  /** متن الحديث الكامل. */
  text: string;
  /** السند المختصر (مثل: "عن أبي هريرة رضي الله عنه"). */
  narrator: string;
  /** الكتاب/المجموعة الأساسية للحديث */
  collection: HadithCollection;
  /** فئة الموضوع (للفلترة بالموضوع) */
  category: HadithCategory;
  /** درجة الحديث (للأحاديث خارج الصحيحين). */
  grade: 'صحيح' | 'حسن' | 'متفق عليه' | 'قدسي';
  /** التخريج التفصيلي (مثلاً: "البخاري 1، مسلم 1907") */
  reference?: string;
}

